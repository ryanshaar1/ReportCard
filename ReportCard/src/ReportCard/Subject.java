package ReportCard;

public class Subject{
	private String subName;
	private int grade;
	
	public void setSubName(String subName) {
		this.subName = subName;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	
	public String getSubName() {
		return this.subName;
	}
	public int getGrade() {
		return this.grade;
	}

}



